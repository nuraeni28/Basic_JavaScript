//create array
const days = ["Monday", "Thuesday", "Wednesday"]
console.log(days)

//access array
console.log(days[1])

//add new item in final element from an array
days.push('Friday')
console.log(days)

//add new item in first element from an array
days.unshift('Thursday')
console.log(days)

//remove the first element from an array 
days.shift()
console.log(days)

//remove the final element from an array
days.pop()
console.log(days)

//copy element
days.splice(1,2)
console.log(days)



                